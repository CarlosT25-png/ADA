    //Aqui verifica que si pierde el foco y el txtCodigo esta vacio se le vuelva a poner Codigo
    private void txtCodigoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtCodigoFocusLost
        if(txtCodigo.getText().equals("")){
            txtCodigo.setText("Codigo");
            txtCodigo.setForeground(new Color(222,221,221));
        }
    }//GEN-LAST:event_txtCodigoFocusLost
 
    //Este evento es para cuando le des el txtCodigo se elimine el texto Codigo automaticamente
    private void txtCodigoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtCodigoMouseClicked
        if(txtCodigo.getText().equals("Codigo")){
            txtCodigo.setText("");
            txtCodigo.setForeground(new Color(56,51,51));   //Aqui cambiamos el color de la fuente de gris a negro
        }
    }//GEN-LAST:event_txtCodigoMouseClicked
    
